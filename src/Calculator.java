public class Calculator {
    public static double exp (double a, double b) {     //hatványozás
        double c = Math.pow(a,b);
        return c;
    }

    public static double sum (double a, double b) {
        double c = a + b;
        return c;
    }

    public static double ext (double a, double b) {
        double c = a - b;
        return  c;
    }


}
